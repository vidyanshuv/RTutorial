# ____Type Casting_______
e = as.character(a) # as.character() method is used for converting data to character
class(e)
f = as.integer(e) # as.integer() method is used for converting data to integer
class(f)
g = as.numeric(f) # as.numeric() method is used for converting data to numeric
class(g)


